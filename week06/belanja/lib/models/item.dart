class Item {
  String name;
  int price;
  int weight;
  String brand;
  String condition;
  int stock;
  String category;
  String description;
  String imageUrl;
  int rating;
  String seller;
  String location;

  Item({
    required this.name,
    required this.price,
    required this.weight,
    required this.brand,
    required this.condition,
    required this.stock,
    required this.category,
    required this.description,
    required this.imageUrl,
    required this.rating,
    required this.seller,
    required this.location,
  });
}
